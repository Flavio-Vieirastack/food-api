CREATE TABLE if not exists city (
    id bigint generated by default as identity,
    state_id bigint,
    name varchar(80),
    primary key (id)
);
CREATE TABLE if not exists group_permissions_tb (
    group_id bigint not null,
    permission_id bigint not null
);
CREATE TABLE if not exists group_permissions (
    id bigint generated by default as identity,
    name varchar(80) not null,
    primary key (id)
);
CREATE TABLE if not exists kitchen (
    id bigint generated by default as identity,
    name varchar(80) not null,
    primary key (id)
);

CREATE TABLE if not exists payment_type (
    id bigint generated by default as identity,
    description varchar(200) not null,
    primary key (id)
);

CREATE TABLE if not exists permission (
    id bigint generated by default as identity,
    description varchar(200) not null,
    name varchar(80) not null,
    primary key (id)
);

CREATE TABLE if not exists product (
    active boolean not null,
    price numeric(38,2) not null,
    id bigint generated by default as identity,
    restaurant_id bigint,
    description varchar(200) not null,
    name varchar(80) not null,
    primary key (id)
);

CREATE TABLE if not exists restaurant (
    delivery_tax numeric(38,2),
    address_city bigint,
    created_at datetime,
    id bigint generated by default as identity,
    kitchen_id bigint,
    updated_at datetime,
    address_complement varchar(80),
    address_district varchar(80),
    address_number varchar(10),
    address_public_place varchar(80),
    address_zip_code varchar(80),
    name varchar(80) not null,
    primary key (id)
);

CREATE TABLE if not exists restaurant_payment_type (
    payment_type_id bigint not null,
    restaurant_id bigint not null
);
CREATE TABLE if not exists restaurant_products (
    products_id bigint not null unique,
    restaurant_id bigint not null
 );
CREATE TABLE if not exists state (
    id bigint generated by default as identity,
    name varchar(80) not null,
    primary key (id)
);
CREATE TABLE if not exists user_group_tb (
    group_id bigint not null,
    user_id bigint not null
);
CREATE TABLE if not exists user_client (
    created_at timestamp(6),
    id bigint generated by default as identity,
    email varchar(80) unique,
    name varchar(80) not null,
    password varchar(255) not null,
    primary key (id)
);